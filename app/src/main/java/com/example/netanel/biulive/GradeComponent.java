public class GradeComponent {
    private String type = "";
    private String grade = "";
    private String moed = "";
    private String precents = "";
    private String date = "";

    public GradeComponent(){
    }


    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getMoed() {
        return moed;
    }

    public void setMoed(String moed) {
        this.moed = moed;
    }

    public String getPrecents() {
        return precents;
    }

    public void setPrecents(String precents) {
        this.precents = precents;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
